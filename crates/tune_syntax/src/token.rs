use tune_diagnostics::Span;

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum TokenKind {
    Whitespace,
    LineComment,
    DocComment,
    Ident,
    IntLiteral,
    FloatLiteral,
    StringLiteral,
    MultilineStringLiteral,
    KeywordLet,
    KeywordStruct,
    KeywordEnum,
    KeywordTag,
    KeywordIf,
    KeywordElif,
    KeywordElse,
    KeywordMatch,
    KeywordFor,
    KeywordIn,
    KeywordWhile,
    KeywordLoop,
    KeywordBreak,
    KeywordContinue,
    KeywordReturn,
    KeywordSpawn,
    KeywordImport,
    KeywordPub,
    KeywordPanic,
    KeywordNever,
    KeywordOk,
    KeywordError,
    KeywordTrue,
    KeywordFalse,
    KeywordNone,
    KeywordSelf,
    KeywordNot,
    KeywordAnd,
    KeywordOr,
    KeywordIs,
    KeywordIsNot,
    At,
    Bang,
    Tilde,
    Amp,
    Pipe,
    Caret,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Equal,
    EqualEqual,
    TildeEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    ShiftLeft,
    ShiftRight,
    Arrow,
    FatArrow,
    Colon,
    ColonColon,
    Dot,
    DotDot,
    Comma,
    Semicolon,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    LeftBracket,
    RightBracket,
    Eof,
    Error,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub kind: TokenKind,
    pub text: String,
    pub span: Span,
}

impl Token {
    #[must_use]
    pub fn new(kind: TokenKind, text: impl Into<String>, span: Span) -> Self {
        Self {
            kind,
            text: text.into(),
            span,
        }
    }
}
